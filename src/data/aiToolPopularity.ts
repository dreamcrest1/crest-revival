const normalise = (s: string) =>
  s.toLowerCase().replace(/[^a-z0-9]/g, '');

const TABLE: Record<string, number> = Object.fromEntries(
  Object.entries(RAW).map(([k, v]) => [normalise(k), v]),
);

/** Estimated monthly Google search volume. Falls back to a small
 *  non-zero default so unmapped products still sort below mapped ones
 *  but above truly empty entries. */
export function popularityFor(name: string): number {
  return TABLE[normalise(name)] ?? 1_000;
}
